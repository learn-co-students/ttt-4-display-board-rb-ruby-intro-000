# Define display_board that accepts a board and prints
# out the current state.

def display_board(boardinput)
  puts " #{boardinput[0]} | #{boardinput[1]} | #{boardinput[2]} "
  puts "-----------"
  puts " #{boardinput[3]} | #{boardinput[4]} | #{boardinput[5]} "
  puts "-----------"
  puts " #{boardinput[6]} | #{boardinput[7]} | #{boardinput[8]} "
end
