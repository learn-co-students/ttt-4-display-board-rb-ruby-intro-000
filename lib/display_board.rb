# Define display_board that accepts a board and prints
# out the current state.
def display_board(result)
  puts " #{result[0]} | #{result[1]} | #{result[2]} "
  puts "-----------"
  puts " #{result[3]} | #{result[4]} | #{result[5]} "
  puts "-----------"
  puts " #{result[6]} | #{result[7]} | #{result[8]} "
end
